package BT.managers;

/**
 *
 * @author Karel Hala
 */
public class CoordinateManager {
    protected int x;
    protected int y;
    
    public int getX()
    {
        return this.x;
    }
    
    public void setX(int X)
    {
        this.x = X;
    }
    
    public int getY()
    {
        return this.y;
    }
    
    public void setY(int Y)
    {
        this.y = Y;
    }
}
